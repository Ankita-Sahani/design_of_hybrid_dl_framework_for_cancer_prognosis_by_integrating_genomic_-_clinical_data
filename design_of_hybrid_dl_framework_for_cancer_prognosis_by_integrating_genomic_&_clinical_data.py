class CNN1D (nn.Module):
  def __init__(self, input_dim):
    super().__init__()
    self.conv1 = nn.Conv1d(1, 32, kernel_size=3)
    self.pool = nn.MaxPool1d(2)
    self.fc1 = nn.Linear((input_dim - 2) // 2 * 32, 1)
  def forward(self, x):
    x = x.unsqueeze(1)
    x = self.pool(torch.relu(self.conv1(x)))
    x = x.view(x.size(0), -1)
    return self.fc1(x)

