# Define the PureLNN model class
class PureLNN(nn.Module):
    def __init__(self, genomic_features_size):
        super().__init__()
        liquid_units = 32 # Use consistent units with HybridLiquidDNN's genomic branch
        self.genomic_projection = nn.Linear(genomic_features_size, liquid_units)
        self.liquid = ncps.LTC(
            input_size=liquid_units, # LTC input size now matches its units
            units=liquid_units # Reduced units for memory footprint
        )
        self.classifier = nn.Sequential(
            nn.Linear(liquid_units, 64),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(64, 1)
        )

    def forward(self, xg):
        # xg is expected to be [batch_size, 1, genomic_features_size]
        xg_projected = self.genomic_projection(xg.squeeze(1)).unsqueeze(1) # Project and maintain sequence dim
        liquid_out, _ = self.liquid(xg_projected)
        # liquid_out will be [batch_size, 1, liquid_units]
        g_feat = liquid_out.squeeze(1) # Remove the sequence_length dimension
        return self.classifier(g_feat).squeeze() # Squeeze to remove single-dim output if present

