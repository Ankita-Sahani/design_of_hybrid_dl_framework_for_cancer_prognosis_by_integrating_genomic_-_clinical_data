import torch.nn as nn

class PureDNN (nn.Module):
  def __init__(self,clinical_features_size):
    super().__init__()
    self.network = nn.Sequential(nn.Linear(clinical_features_size,64),nn.ReLU(),nn.Dropout(0.3),nn.Linear(64,32),nn.ReLU(),nn.Dropout(0.3),nn.Linear(32,1))
# Output layer for binary classification

  def forward(self,xc):
    return self.network(xc).squeeze()
# Squeeze to [batch_size] for BCEWithLogitsLoss

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader, TensorDataset
import numpy as np
import time
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
# Ensure that Xc_train, Xc_test, y_train_tensor, y_test_tensor are available from previous steps
# and PureDNN class is defined.
# 1. Define clinical_features_size
clinical_features_size = Xc_train.shape[-1]
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
# 2. Initialize the PureDNN model
pure_dnn_model = PureDNN(clinical_features_size=clinical_features_size).to(device)
# 3. Define the loss function and optimizer
criterion = nn.BCEWithLogitsLoss()
optimizer = optim.Adam(pure_dnn_model.parameters(), lr=0.001)
# 4. Create DataLoader instances
# Make sure y_train and y_test are properly converted for DNN
Xc_train_dnn_tensor = torch.FloatTensor(Xc_train).to(device)
y_train_dnn_tensor = torch.FloatTensor(y_train.values).to(device) # Using y_train pandas Series

Xc_test_dnn_tensor = torch.FloatTensor(Xc_test).to(device)
y_test_dnn_tensor = torch.FloatTensor(y_test.values).to(device) # Using y_test pandas Series

# --- DEBUGGING PRINTS ---
print(f"DEBUG: Shape of Xc_test: {Xc_test.shape}")
print(f"DEBUG: Shape of y_test.values: {y_test.values.shape}")
print(f"DEBUG: Shape of Xc_test_dnn_tensor: {Xc_test_dnn_tensor.shape}")
print(f"DEBUG: Shape of y_test_dnn_tensor: {y_test_dnn_tensor.shape}")
# --- END DEBUGGING PRINTS ---

train_dataset_dnn = TensorDataset(Xc_train_dnn_tensor, y_train_dnn_tensor)
train_loader_dnn = DataLoader(train_dataset_dnn, batch_size=32, shuffle=True)
test_dataset_dnn = TensorDataset(Xc_test_dnn_tensor, y_test_dnn_tensor)
test_loader_dnn = DataLoader(test_dataset_dnn, batch_size=32, shuffle=False)
EPOCHS_DNN = 60
# Ensuring a sufficient number of epochs for fair comparison
print(f"Starting training for Pure DNN model on {device}...")
pure_dnn_loss_history = []
# List to store training loss per epoch

pure_dnn_test_loss_history = []
# List to store test loss per epoch
# 5. Implement a training loop and 6. Measure training time
start_time_dnn = time.time()
for epoch in range(EPOCHS_DNN):
  pure_dnn_model.train()
  total_train_loss = 0
  for xc, y in train_loader_dnn:
    xc, y = xc.to(device), y.to(device)
    optimizer.zero_grad()
    outputs = pure_dnn_model(xc)
    loss = criterion(outputs, y)
    loss.backward()
    optimizer.step()
    total_train_loss += loss.item()

  avg_train_loss = total_train_loss / len(train_loader_dnn)
  pure_dnn_loss_history.append(avg_train_loss)

  # Store average training loss for the epoch
  # --- Evaluate on test set ---
  pure_dnn_model.eval()
  total_test_loss = 0
  with torch.no_grad():
    for xc, y in test_loader_dnn:
      xc, y = xc.to(device), y.to(device)
      outputs = pure_dnn_model(xc)
      loss = criterion(outputs, y)
      total_test_loss += loss.item()
  avg_test_loss = total_test_loss / len(test_loader_dnn)
  pure_dnn_test_loss_history.append(avg_test_loss)

  # Store average test loss for the epoch

  pure_dnn_model.train()

  # Set model back to training mode
  if epoch % 10 == 0:
    print(f"Epoch{epoch+1}/{EPOCHS_DNN}|Train Loss:{avg_train_loss:.4f}|Test Loss:{avg_test_loss:.4f}")

end_time_dnn = time.time()
pure_dnn_training_time = end_time_dnn - start_time_dnn
print(f"Pure DNN training finished in {pure_dnn_training_time:.2f} seconds.")

# 7. After training, set the model to evaluation mode
pure_dnn_model.eval()
# 8. Iterate through the test DataLoader to make predictions and collect true labels and predicted probabilities
y_true_dnn, y_prob_dnn = [], []
with torch.no_grad():
  for xc, y in test_loader_dnn:
    xc, y = xc.to(device), y.to(device)
