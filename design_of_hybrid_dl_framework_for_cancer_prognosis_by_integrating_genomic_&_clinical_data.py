"""# Hybrid Model(LNN-DNN)"""

class HybridLiquidDNN(nn.Module):
  def __init__(self, genomic_features, clinical_features):
    super().__init__()
    liquid_units = 32 # Further reduced units for the liquid layer
    self.genomic_projection = nn.Linear(genomic_features, liquid_units) # Project genomic features
    self.liquid = ncps.LTC(
        input_size=liquid_units, # LTC input size now matches its units
        units=liquid_units # Reduced units for memory footprint
    )
    self.clinical_net = nn.Sequential(
        nn.Linear(clinical_features, 32),
        nn.ReLU()
    )
    # Classifier input size adjusted to new liquid output size (liquid_units) + clinical features
    self.classifier = nn.Sequential(nn.Linear(liquid_units + 32, 64), nn.ReLU(), nn.Dropout(0.3), nn.Linear(64, 1))

  def forward(self, xg, xc):
    # xg is [batch_size, 1, genomic_features]
    # Squeeze, project, then unsqueeze for the LTC layer
    xg_projected = self.genomic_projection(xg.squeeze(1)).unsqueeze(1) # Output: [batch_size, 1, liquid_units]
    liquid_out, _ = self.liquid(xg_projected)
    # liquid_out will be [batch_size, 1, liquid_units]
    g_feat = liquid_out.squeeze(1)
    # Remove the sequence_length dimension, g_feat is [batch_size, liquid_units]
    c_feat = self.clinical_net(xc)
    # c_feat is [batch_size, 32]
    fused = torch.cat((g_feat, c_feat), dim=1)
    # Concatenates [batch_size, (liquid_units + 32)]
    return self.classifier(fused).squeeze()

